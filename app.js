        // Triggers the multi-tenant redirection simulation modal
        function triggerRedirection(cityName) {
            const modal = document.getElementById('redirection-modal');
            const title = document.getElementById('redir-title');
            const desc = document.getElementById('redir-desc');
            const urlLabel = document.getElementById('target-url-label');
            const launchBtnText = document.getElementById('modal-launch-btn').querySelector('span');

            const lowerCity = cityName.toLowerCase();
            targetQuestUrl = `https://www.lorenzofornara.it/${lowerCity}-quest`;

            // Translate modal fields based on selections
            if (cityName === 'Bordighera') {
                title.textContent = translations[currentLang].modal_redir_loading_bordighera;
                desc.textContent = translations[currentLang].modal_redir_desc_bordighera;
            } else {
                title.textContent = translations[currentLang].modal_redir_loading_vallecrosia;
                desc.textContent = translations[currentLang].modal_redir_desc_vallecrosia;
            }

            urlLabel.textContent = `www.lorenzofornara.it/${lowerCity}`;
            launchBtnText.textContent = translations[currentLang].modal_launch_btn;

            modal.classList.remove('hidden');

            // Auto-redirect after showing the loading animation (~2.5s).
            // Clear any previously scheduled redirect to avoid duplicate triggers.
            if (redirectTimeoutId) {
                clearTimeout(redirectTimeoutId);
            }
            redirectTimeoutId = setTimeout(() => {
                redirectTimeoutId = null;
                window.location.href = targetQuestUrl;
            }, 2500);
        }

        function openTargetQuest() {
            // User clicked manually: cancel the pending auto-redirect
            if (redirectTimeoutId) {
                clearTimeout(redirectTimeoutId);
                redirectTimeoutId = null;
            }
            // Open target URL simulation window
            window.open(targetQuestUrl, '_blank');
            closeRedirection();
        }

        function closeRedirection() {
            // Cancel any pending auto-redirect when the modal is dismissed
            if (redirectTimeoutId) {
                clearTimeout(redirectTimeoutId);
                redirectTimeoutId = null;
            }
            document.getElementById('redirection-modal').classList.add('hidden');
        }

        function simulateQuest(cityName) {
            const modal = document.getElementById('simulation-modal');
            const title = document.getElementById('modal-city-title');
            const desc = document.getElementById('modal-city-desc');
            const list = document.getElementById('poi-list');

            title.textContent = cityName + " Quest";
            desc.textContent = cityDescs[cityName];
            
            list.innerHTML = '';
            cityPOIs[cityName].forEach(poi => {
                const li = document.createElement('li');
                li.className = "flex items-center gap-2 py-1.5 border-b border-slate-200/40 dark:border-slate-800/20 text-slate-800 dark:text-slate-200";
                li.innerHTML = `
                    <svg class="w-4 h-4 text-indigo-500 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2.5" d="M9 5l7 7-7-7" />
                    </svg>
                    <span>${poi}</span>
                `;
                list.appendChild(li);
            });

            modal.classList.remove('hidden');
        }

