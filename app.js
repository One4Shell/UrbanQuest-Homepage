// =============== PRIVACY POLICY MODAL ===============
function openPrivacy() {
    const modal = document.getElementById('privacy-modal');
    if (modal) {
        modal.classList.remove('hidden');
        document.body.style.overflow = 'hidden';
        // Move focus inside for accessibility
        const closeBtn = modal.querySelector('button[onclick="closePrivacy()"]');
        if (closeBtn) closeBtn.focus();
    }
}

function closePrivacy() {
    const modal = document.getElementById('privacy-modal');
    if (modal) {
        modal.classList.add('hidden');
        document.body.style.overflow = '';
    }
}

// Close privacy modal with ESC key (and other modals for consistency)
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') {
        const privacyModal = document.getElementById('privacy-modal');
        if (privacyModal && !privacyModal.classList.contains('hidden')) {
            closePrivacy();
        }
    }
});