// Set Language Logic
function changeLang(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.getElementById('current-lang-label').textContent = lang.toUpperCase();

    // Loop and translate
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Close dropdown
    document.getElementById('lang-dropdown').classList.add('hidden');
}

// Dropdown toggle
function toggleLangDropdown() {
    const dropdown = document.getElementById('lang-dropdown');
    dropdown.classList.toggle('hidden');
}

// Close dropdown when clicking outside
window.addEventListener('click', function(e) {
    const menu = document.getElementById('lang-menu');
    if (menu && !menu.contains(e.target)) {
        const dropdown = document.getElementById('lang-dropdown');
        if (dropdown) dropdown.classList.add('hidden');
    }
});

// Theme System Logic (Dark/Light)
function toggleDarkMode() {
    const isDark = document.documentElement.classList.contains('dark');
    if (isDark) {
        document.documentElement.classList.remove('dark');
        localStorage.setItem('theme', 'light');
        updateThemeIcons('light');
    } else {
        document.documentElement.classList.add('dark');
        localStorage.setItem('theme', 'dark');
        updateThemeIcons('dark');
    }
}

function updateThemeIcons(theme) {
    const sun = document.getElementById('theme-icon-light');
    const moon = document.getElementById('theme-icon-dark');
    if (theme === 'dark') {
        sun.classList.remove('hidden');
        moon.classList.add('hidden');
    } else {
        sun.classList.add('hidden');
        moon.classList.remove('hidden');
    }
}

// On Load theme check
window.onload = function() {
    const savedTheme = localStorage.getItem('theme');
    const systemPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;

    if (savedTheme === 'dark' || (!savedTheme && systemPrefersDark)) {
        document.documentElement.classList.add('dark');
        updateThemeIcons('dark');
    } else {
        document.documentElement.classList.remove('dark');
        updateThemeIcons('light');
    }

    // Set default translation
    changeLang('it');
}

